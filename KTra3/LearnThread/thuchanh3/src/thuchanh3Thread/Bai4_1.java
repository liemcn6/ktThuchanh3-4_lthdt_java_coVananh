    class Coloring extends Thread{
    
    public void run(){
       String line="Running...\n";
       
        while(true){
            
         jTextArea1.setText(line);
         count++;
         line+="Running...\n";
                try{
                    sleep(500);
                }catch(InterruptedException e){
                    System.out.println(e);
                }
                }
  
    }
}
    class Coloring1 extends Thread{
    
    public void run(){
       
       boolean flag1=false;
       
        while(true){
            if(!flag1){
         jTextArea1.setForeground(Color.red);
         flag1=true;
            }else{
                jTextArea1.setForeground(Color.green);
                flag1=false;
            }
                try{
                    sleep(400);
                }catch(InterruptedException e){
                    System.out.println(e);
                }
                }
  
    }
}

