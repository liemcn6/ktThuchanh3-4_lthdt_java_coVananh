    class running extends Thread{
        public void run(){
            int vl=jSlider1.getValue();
            Font f=jLabel1.getFont();
            while(true){
                if(vl>=jSlider1.getMaximum())
                    vl=jSlider1.getMinimum();
                vl+=2;
                jSlider1.setValue(vl);
                jLabel1.setFont(new Font(f.getFontName(), f.getStyle(), vl));
                try{
                  sleep(200);
                }catch(InterruptedException e){
                    System.out.println(e);
                }
            }
            
        }
    }

