    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r=jTable1.getSelectedRow();
        
        if(r<0||r>(tm.getRowCount()-1)){
            JOptionPane.showMessageDialog(this, "vui long chon dong sua!!", 
                    "warning", JOptionPane.WARNING_MESSAGE);
        }else{
            dongsua=r;
        SuaDialog sd=new SuaDialog(this, true);
        sd.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int r=jTable1.getSelectedRow();
        
        if(r<0||r>(tm.getRowCount()-1)){
            JOptionPane.showMessageDialog(this, "vui long chon dong xoa!!", 
                    "warning", JOptionPane.WARNING_MESSAGE);
        }else{
            tm.removeRow(r);
       
        }
        if(tm.getRowCount()<1){
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            String st="So luong sach:";
            jLabel1.setText(st+tm.getRowCount());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

