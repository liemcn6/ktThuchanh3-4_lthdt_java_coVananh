    public void them(Sach s){
        String text="So luong sach:";
        
        tm.addRow(s.toObject());
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);
        
        jLabel1.setText(text+tm.getRowCount());
    }
    public Sach send(){
        Sach s=new Sach();
        s.setMa( tm.getValueAt(dongsua, 0).toString());
        s.setTen(tm.getValueAt(dongsua, 1).toString());
        s.setSoluong(Integer.parseInt(tm.getValueAt(dongsua, 2).toString()));
        s.setDongia(Double.parseDouble(tm.getValueAt(dongsua, 3).toString()));
        return s;
    }
    public void sua(Sach s){
        tm.setValueAt(s.getMa(), dongsua, 0);
        tm.setValueAt(s.getTen(), dongsua, 1);
        tm.setValueAt(s.getSoluong(), dongsua, 2);
        tm.setValueAt(s.getDongia(), dongsua, 3);
        int sl=Integer.parseInt(tm.getValueAt(dongsua, 2).toString());
        double dg=Double.parseDouble(tm.getValueAt(dongsua, 3).toString());
        double gia=sl*dg;
                
        tm.setValueAt(gia, dongsua, 4);
    }
    

