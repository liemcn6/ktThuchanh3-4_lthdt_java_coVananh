    private boolean isSoDep(long c){
        long d=c;
        long r,sum=0;
        while(c>0){
            r=c%10;
            sum=(sum*10)+r;
            c/=10;
        }
        if(d==sum) return true;
        else return false;
       
    }
    private String getEmail(String ten){
        //String ten="Nguyen Quang Liem";
        String email="";
        
        email=ten.substring(ten.lastIndexOf(" ")+1,(ten.length()));
        
        //System.out.println(email);
        String[] g=ten.split("\\s+");
        for (int i = 0; i < g.length-1; i++) {
            email=email+g[i].charAt(0);
        }
        email+="@ptit.edu.vn";
        //System.out.println(email);
        
        return email;
    }
