    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        buttonFalse();them=true;
        File f=new File(file);
        if(!ok){
            if(f.exists()){
                List<ViTRi> l=IOFlile.docra(file);
                if(l.size()>0){
                    ViTRi.setSma(l.get(l.size()-1).getMa()+1);
                }
            }
            ok=true;
        }
        jTextField1.setText(ViTRi.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(them){
            ViTRi c=new ViTRi();
            c.setTen(jTextField2.getText());
            c.setMt(jTextField3.getText());
            //c.setMl(jTextField4.getText());
            tm.addRow(c.toObject());
            them=false;
            buttonTrue();
        }
        if(sua){
             int r=jTable1.getSelectedRow();
             tm.setValueAt(jTextField2.getText(), r, 1);
             tm.setValueAt(jTextField3.getText(), r, 2);
             sua=false;
             buttonTrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

