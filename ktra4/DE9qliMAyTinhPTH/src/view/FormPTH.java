    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        List<PTHucHanh> list=new ArrayList<>();
        for(int i=0;i<tm.getRowCount();i++){
            int ma=Integer.parseInt(tm.getValueAt(i, 0).toString());
            String ten=tm.getValueAt(i, 1).toString();
            String sm=tm.getValueAt(i, 2).toString();
            //String nam=tm.getValueAt(i, 3).toString();
            //String ch=tm.getValueAt(i, 4).toString();
            PTHucHanh m=new PTHucHanh(ma, ten, sm);
           
            list.add(m);
        }
        IOFile.vietvao(file, list);
    }//GEN-LAST:event_jButton5ActionPerformed

