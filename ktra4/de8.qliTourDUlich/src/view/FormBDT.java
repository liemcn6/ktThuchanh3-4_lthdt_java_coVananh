    private KhachHang getKhbyma(int ma){
        List<KhachHang> l=IOFile.docra("src/controller/kh.dat");
        for(KhachHang i:l){
            if(i.getMa()==ma) return i;
        }
        return null;
    }
    private Tour getTbyma(int ma){
       List<Tour> l1=IOFile.docra("src/controller/t.dat");
       for(Tour i:l1){
            if(i.getMa()==ma) return i;
        }
        return null;
    }
    private List<BDTour> getAll(){
         List<BDTour> list=new ArrayList<>();
         for (int i = 0; i < tm.getRowCount(); i++) {
            int makh=Integer.parseInt(tm.getValueAt(i, 0).toString());
            int mat=Integer.parseInt(tm.getValueAt(i, 3).toString());
            KhachHang k=getKhbyma(makh);
            Tour t=getTbyma(mat);
            String sn=tm.getValueAt(i, 5).toString();
            String nbd=tm.getValueAt(i, 6).toString();
            
            BDTour b=new BDTour(k, t, sn, nbd);
            list.add(b);
        }
         return list;
    }
    private boolean existed(int makh,String nbd){
        List<BDTour> list=getAll();
        for(BDTour i:list){
            if(i.getK().getMa()==makh&&i.getNbd()==nbd) return true;
            
        }
        return false;
    }

