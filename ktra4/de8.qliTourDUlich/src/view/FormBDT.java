    private void jComboBox4ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox4ItemStateChanged
        // TODO add your handling code here:
         List<BDTour> list=getAll();
         if(jComboBox4.getSelectedIndex()==0){
             //kieu kh
             list.sort(new Comparator<BDTour>() {
                 @Override
                 public int compare(BDTour o1, BDTour o2) {
                     return o1.getK().getKieu().compareToIgnoreCase(o2.getK().getKieu());
                 }
             });
         }else{
              list.sort(new Comparator<BDTour>() {
                 @Override
                 public int compare(BDTour o1, BDTour o2) {
                     return o1.getT().getKieu().compareToIgnoreCase(o2.getT().getKieu());
                 }
             });
         }
         tm.setRowCount(0);
         for(BDTour i:list){
             tm.addRow(i.toObject());
         }
    }//GEN-LAST:event_jComboBox4ItemStateChanged

