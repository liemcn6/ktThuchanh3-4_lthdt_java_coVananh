    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(them){
            Tour t=new Tour();
            t.setKieu(jComboBox1.getSelectedItem().toString());
            t.setTt(jTextArea1.getText());
            t.setGia(jTextField2.getText());
            tm.addRow(t.toObject());
            them=false;
            buttonTrue();
        }
        if(sua){
            int r=jTable1.getSelectedRow();
            tm.setValueAt(jComboBox1.getSelectedItem().toString(),r, 1);
            tm.setValueAt(jTextArea1.getText(), r, 2);
            tm.setValueAt(jTextField2.getText(), r, 3);
            sua=false;
            buttonTrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        buttonFalse();them=true;
        File f=new File(file);
        if(!ok){
            if(f.exists()){
                List<Tour> list=IOFile.docra(file);
                if(list.size()>0){
                    Tour.setSma(list.get(list.size()-1).getMa()+1);
                }
           }
            ok=true;
        }
        jTextField1.setText(Tour.getSma()+"");
        jComboBox1.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

