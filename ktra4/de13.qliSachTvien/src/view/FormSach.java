    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        them=true;
        buttonFalse();
        File f=new File(file);
        if(!ok){
            if(f.exists()){
                List<Sach> l=IOFile.docra(file);
                if(l.size()>0){
                    Sach.setSma(l.get(l.size()-1).getMa()+1);
                }
                
            }ok=true;
        }
        jTextField1.setText(Sach.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(them){
            Sach s=new Sach();
            s.setTen(jTextField2.getText());
            s.setTg(jTextField3.getText());
            s.setCn(jComboBox1.getSelectedItem().toString());
            s.setNxb(jTextField4.getText());
            tm.addRow(s.toObject());
            them=false;
            buttonTrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

