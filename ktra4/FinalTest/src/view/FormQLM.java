    private Reader getReaderbyMa(int ma){
        List<Reader> list= IOFile.docra("src/controller/reader.dat");
        for(Reader i:list){
            if(i.getMa()==ma){
                return i;
            }
        }
        return null;
    }
    private Book getBookbyma(int ma){
        List<Book> list= IOFile.docra("src/controller/book.dat");
        for(Book i:list){
            if(i.getMa()==ma){
                return i;
            }
        }
            return null;
    }
    private List<QLMS> getAll(){
        List<QLMS> list= new ArrayList<>();
        for(int i=0;i<tm.getRowCount();i++){
            int mbd=Integer.parseInt(tm.getValueAt(i, 0).toString());
            int mas=Integer.parseInt(tm.getValueAt(i, 2).toString());
            Reader r=getReaderbyMa(mbd);
            Book b=getBookbyma(mas);
            int sl=Integer.parseInt(tm.getValueAt(i, 3).toString());
            String tt=tm.getValueAt(i, 4).toString();
            QLMS q=new QLMS(r, b, sl, tt);
            list.add(q);
            
        }
        return list;
    }
        
    private boolean existed(int mabd,int mas){
        List<QLMS> list= getAll();
        for(QLMS i:list){
            if(i.getReader().getMa()==mabd&&i.getBook().getMa()==mas){
                return true;
            }
        }
        return false;
    }
    private int getDauSach(int mabd){
        int count=0;
        List<QLMS> list=getAll();
        for(QLMS i: list){
            if(i.getReader().getMa()==mabd)
                count++;
        }
        return count;
    }

