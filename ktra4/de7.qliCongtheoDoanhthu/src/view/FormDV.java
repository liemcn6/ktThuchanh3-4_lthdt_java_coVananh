    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        them=true;buttonFalse();
        File f=new File(file);
        if(!ok){
            if(f.exists()){
                List<DichVu> l=IOFile.docra(file);
                if(l.size()>0){
                    DichVu.setSma(l.get(l.size()-1).getMa()+1);
                }
            }
            ok=true;
        }
        jTextField1.setText(DichVu.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(them){
            DichVu d=new DichVu();
            d.setTen(jTextField2.getText());
            d.setNhom(jComboBox1.getSelectedItem().toString());
            d.setCp(jTextField3.getText());
            d.setGia(jTextField4.getText());
            tm.addRow(d.toObject());
            them=false;
            buttonTrue();
        }
        if(sua){
            int r=jTable1.getSelectedRow();
            tm.setValueAt(jTextField2.getText(), r, 1);
            tm.setValueAt(jComboBox1.getSelectedItem().toString(), r, 2);
            tm.setValueAt(jTextField3.getText(), r, 3);
            tm.setValueAt(jTextField4.getText(), r, 4);
            sua=false;
            buttonTrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

