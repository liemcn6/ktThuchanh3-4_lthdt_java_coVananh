    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
         List<BTC> l=getAll();
         if(jRadioButton1.isSelected()){
             l.sort(new Comparator<BTC>() {
                 @Override
                 public int compare(BTC o1, BTC o2) {
                     String t1=o1.getC().getTen();
                      String t2=o2.getC().getTen();
                      String ten1=t1.substring(t1.lastIndexOf(" ")+1)+t1;
                      String ten2=t2.substring(t2.lastIndexOf(" ")+1)+t2;
                      return ten1.compareToIgnoreCase(ten2);
                 }
             });
         }
         else{
             l.sort(new Comparator<BTC>() {
                 @Override
                 public int compare(BTC o1, BTC o2) {
                     return o2.getSl()-o1.getSl();
                 }
             });
         }
         tm.setRowCount(0);
        for(BTC i:l){
            tm.addRow(i.toObject());
        }
    }//GEN-LAST:event_jButton8ActionPerformed


