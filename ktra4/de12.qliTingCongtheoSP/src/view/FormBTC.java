    private CongNhan getCnbyma(int ma){
        List<CongNhan> l=IOFlie.docra("src/controller/cn.dat");
        
        for(CongNhan i:l){
            if(i.getMa()==ma) return i;
        }
        return null;
    }
    private SanPham getSpbyma(int ma){
        List<SanPham> l=IOFlie.docra("src/controller/sp.dat");
        
        for(SanPham i:l){
            if(i.getMa()==ma) return i;
        }
        return null;
    }
    private List<BTC> getAll(){
        List<BTC> l=new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
            int macn=Integer.parseInt(tm.getValueAt(i, 0).toString());
            int masp=Integer.parseInt(tm.getValueAt(i, 2).toString());
            CongNhan c=getCnbyma(macn);
            SanPham s=getSpbyma(masp);
            int sl=Integer.parseInt(tm.getValueAt(i, 3).toString());
            BTC b=new BTC(c, s, sl);
            l.add(b);
        }
        return l;
    }
    private boolean existed(int macn,int masp){
        List<BTC> l=getAll();
        for(BTC i:l){
            if(i.getC().getMa()==macn&&i.getS().getMa()==masp) return true;
        }
        return false;
    }
    private int getDauSp(int macn){
        List<BTC> l=getAll();
        int c=0;
        for(BTC i:l){
            if(i.getC().getMa()==macn) c++;
        }
        return c;
    }

