    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(them){
            NhanVien n=new NhanVien();
            n.setHt(jTextField2.getText());
            n.setDc(jTextField3.getText());
            n.setSdt(jTextField4.getText());
            n.setBacLuong(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
            tm.addRow(n.toObject());
            them=false;
            buttonTrue();
        }
        if(sua){
            int r=jTable1.getSelectedRow();
            tm.setValueAt(jTextField2.getText(), r, 1);
            tm.setValueAt(jTextField3.getText(), r, 2);
            tm.setValueAt(jTextField4.getText(), r, 3);
            tm.setValueAt(jComboBox1.getSelectedItem().toString(), r, 4);
            sua=false;
            buttonTrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

