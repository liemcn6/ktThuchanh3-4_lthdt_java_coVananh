    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        List<DSCC> list=getAll();
        if(jRadioButton1.isSelected()){
            //ten nv
            list.sort(new Comparator<DSCC>() {
                @Override
                public int compare(DSCC o1, DSCC o2) {
                    String t1=o1.getN().getHt();
                    String t2=o2.getN().getHt();
                    String ten1=t1.substring(t1.lastIndexOf(" ")+1)+t1;
                    String ten2=t2.substring(t2.lastIndexOf(" ")+1)+t2;
                    return ten1.compareToIgnoreCase(ten2);
                }
            });
        }else{
            list.sort(new Comparator<DSCC>() {
                @Override
                public int compare(DSCC o1, DSCC o2) {
                    String t1=o1.getP().getTen();
                    String t2=o2.getP().getTen();
                    
                    return t1.compareToIgnoreCase(t2);
                }
            });
        }
        tm.setRowCount(0);
        for(DSCC i:list){
            tm.addRow(i.toObject());
        }
    }//GEN-LAST:event_jButton7ActionPerformed


