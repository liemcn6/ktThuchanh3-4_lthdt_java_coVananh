    private NhanVien getNvbyMa(int ma){
        List<NhanVien> list=IOFile.docra("src/controller/nv.dat");
        for(NhanVien i:list){
            if(i.getMa()==ma){
                return i;
            }
        }
        return null;
    }
    private PhongBan getPbbyMa(int ma){
        List<PhongBan> list=IOFile.docra("src/controller/pb.dat");
        for(PhongBan i:list){
            if(i.getMa()==ma){
                return i;
            }
        }
        return null;
    }
    private List<DSCC> getAll(){
        List<DSCC> list=new ArrayList<>();
        for(int i=0;i<tm.getRowCount();i++){
            int manv=Integer.parseInt(tm.getValueAt(i, 0).toString());
            int mapb=Integer.parseInt(tm.getValueAt(i, 2).toString());
            NhanVien n=getNvbyMa(manv);
            PhongBan p=getPbbyMa(mapb);
            int sn=Integer.parseInt(tm.getValueAt(i, 4).toString());
            DSCC d=new DSCC(n, p, sn);
            list.add(d);
        }
        return list;
    }
    private boolean existed(int manv,int mapb){
        List<DSCC> list=getAll();
        for(DSCC i:list){
            if(i.getN().getMa()==manv&&i.getP().getMa()==mapb) return true;
        }
        return false;
    }

