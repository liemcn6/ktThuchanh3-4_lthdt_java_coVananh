    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        List<QLMS> list=getAll();
        if(jRadioButton1.isSelected()){
            //sl
            list.sort(new Comparator<QLMS>() {
                @Override
                public int compare(QLMS o1, QLMS o2) {
                   return o1.getSl()-o2.getSl();
                }
            });
        }else{
            list.sort(new Comparator<QLMS>() {
                @Override
                public int compare(QLMS o1, QLMS o2) {
                   String t1=o1.getB().getHt();
                   String t2=o2.getB().getHt();
                   
                   String ten1=t1.substring(t1.lastIndexOf(" ")+1)+t1;
                   String ten2= t2.substring(t2.lastIndexOf(" ")+1)+t2;
                   return ten1.compareToIgnoreCase(ten2);
                  }

            });
        }
        tm.setRowCount(0);
        for(QLMS i:list){
            tm.addRow(i.toObject());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

