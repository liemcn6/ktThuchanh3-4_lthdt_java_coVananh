    private Sach getSachbyMa(int ma){
        List<Sach> list=IOFile.docra("src/controller/sach.dat");
        for(Sach i:list){
            if(i.getMa()==ma){
                return i;
            }
        }
        return null;
    }
    private BanDoc getbdbyMa(int ma){
        List<BanDoc> list=IOFile.docra("src/controller/bandoc.dat");
        for(BanDoc i:list){
            if(i.getMa()==ma){
                return i;
            }
        }
        return null;
    }
    private List<QLMS> getAll(){
        List<QLMS> list=new ArrayList<>();
        for(int i=0;i<tm.getRowCount();i++){
            int mabd=Integer.parseInt(tm.getValueAt(i, 0).toString());
            int mas=Integer.parseInt(tm.getValueAt(i, 2).toString());
            BanDoc b=getbdbyMa(mabd);
            Sach s=getSachbyMa(mas);
            int sl=Integer.parseInt(tm.getValueAt(i,3).toString());
            String tt=tm.getValueAt(i, 4).toString();
            QLMS q=new QLMS(b, s, sl, tt);
            list.add(q);
        }
        return list;
    }
    private boolean existed(int mabd,int mas){
        List<QLMS> list=getAll();
        for(QLMS i:list){
            if(i.getB().getMa()==mabd&&i.getS().getMa()==mas)
                return true;
        }
        return false;
    }
    private int getDauSach(int mabd){
        int count=0;
        List<QLMS> list=getAll();
        for(QLMS i:list){
            if(i.getB().getMa()==mabd) count++;
        }
        return count;
    }

