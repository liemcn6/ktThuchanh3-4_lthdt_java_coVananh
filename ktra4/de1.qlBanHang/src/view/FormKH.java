    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        List<KhachHang> list=new ArrayList<>();
        for(int i=0;i<tm.getRowCount();i++){
            int ma=Integer.parseInt(tm.getValueAt(i, 0).toString());
            String ht=tm.getValueAt(i, 1).toString();
            String dc=tm.getValueAt(i, 2).toString();
            String sdt=tm.getValueAt(i,3).toString();
           KhachHang k=new KhachHang(ma, ht, dc, sdt);
            list.add(k);
        }
        IOFile.vietvao(file, list);
    }//GEN-LAST:event_jButton5ActionPerformed

