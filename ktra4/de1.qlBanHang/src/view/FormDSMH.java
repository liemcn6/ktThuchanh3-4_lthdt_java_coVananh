    private void jComboBox3ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox3ItemStateChanged
        // TODO add your handling code here:
        List<DSMH> list=getAll();
        if(jComboBox3.getSelectedIndex()==0){
            //ten kh
            list.sort(new Comparator<DSMH>() {
                @Override
                public int compare(DSMH o1, DSMH o2) {
                   String t1=o1.getK().getHt();
                   String t2=o2.getK().getHt();
                   String ten1=t1.substring(t1.lastIndexOf(" ")+1)+t1;
                   String ten2=t2.substring(t2.lastIndexOf(" ")+1)+t2;
                   return ten1.compareToIgnoreCase(ten2);
                }
            });
        }else{
            list.sort(new Comparator<DSMH>() {
                @Override
                public int compare(DSMH o1, DSMH o2) {
                   String t1=o1.getM().getTen();
                   String t2=o2.getM().getTen();
                   //String ten1=t1.substring(t1.lastIndexOf(" ")+1)+t1;
                  // String ten2=t2.substring(t2.lastIndexOf(" ")+1)+t2;
                   return t1.compareToIgnoreCase(t2);
                }
            });
        }
        tm.setRowCount(0);
        for(DSMH i:list){
            tm.addRow(i.toObject());
        }
    }//GEN-LAST:event_jComboBox3ItemStateChanged


