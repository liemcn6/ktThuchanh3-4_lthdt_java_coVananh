    private MatHang getMhbyma(int ma){
        List<MatHang> list=IOFile.docra("src/controller/mh.dat");
        for(MatHang i:list){
            if(i.getMa()==ma)
                return i;
        }
        return null;
    }
    private KhachHang getKhbyma(int ma){
        List<KhachHang> list=IOFile.docra("src/controller/kh.dat");
        for(KhachHang i:list){
            if(i.getMa()==ma)
                return i;
        }
        return null;
    }
    private List<DSMH> getAll(){
        List<DSMH> list=new ArrayList<>();
        for(int i=0;i<tm.getRowCount();i++){
            int makh=Integer.parseInt(tm.getValueAt(i, 0).toString());
            int mamh=Integer.parseInt(tm.getValueAt(i, 2).toString());
            KhachHang k=getKhbyma(makh);
            MatHang m=getMhbyma(mamh);
            int sl=Integer.parseInt(tm.getValueAt(i, 4).toString());
            DSMH d=new DSMH(k, m, sl);
            list.add(d);
        }
        return list;
    }
    private boolean existed(int makh,int mamh){
        List<DSMH> list=getAll();
        for(DSMH i:list){
            if(i.getK().getMa()==makh&&i.getM().getMa()==mamh){
                return true;
            }
        }
        return false;
    }
    private int getMatHang(int makh){
        int count=0;
        List<DSMH> list=getAll();
        for(DSMH i:list){
            if(i.getK().getMa()==makh)
                count++;
        }
        return count;
    }

