    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        them=true;
        buttonFalse();
        File f=new File(file);
        if(!ok){
            if(f.exists()){
                List<GiangVien> list=IoFile.docra(file);
                if(list.size()>0){
                    GiangVien.setSma(list.get(list.size()-1).getMa()+1);
                }
            }
            ok=true;
        }
        jTextField1.setText(GiangVien.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(them){
            GiangVien m=new GiangVien();
            m.setTen(jTextField2.getText());
            //m.setTst(Integer.parseInt(jTextField3.getText()));
            m.setDc(jTextField3.getText());
            m.setTd(jComboBox1.getSelectedItem().toString());
            tm.addRow(m.toObject());
            them=false;
            buttonTrue();
        }
        if(sua){
            int r=jTable1.getSelectedRow();
            tm.setValueAt(jTextField2.getText(), r, 1);
            tm.setValueAt(jTextField3.getText(), r, 2);
            tm.setValueAt(jComboBox1.getSelectedItem().toString(), r, 3);
            sua=false;
            buttonTrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

