    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        them=true;
        buttonFalse();
        File f=new File(file);
        if(!ok){
            if(f.exists()){
                List<MOnHOc> list=IoFile.docra(file);
                if(list.size()>0){
                    MOnHOc.setSma(list.get(list.size()-1).getMa()+1);
                }
            }
            ok=true;
        }
        jTextField1.setText(MOnHOc.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(them){
            MOnHOc m=new MOnHOc();
            m.setTen(jTextField2.getText());
            m.setTst(Integer.parseInt(jTextField3.getText()));
            m.setLt(jTextField4.getText());
            m.setKp(jTextField5.getText());
            tm.addRow(m.toObject());
            them=false;
            buttonTrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

