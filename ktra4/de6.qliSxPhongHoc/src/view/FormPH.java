    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        them=true; buttonFalse();
        File f=new File(file);
        if(!ok){
            if(f.exists()){
                List<PhongHoc> l=IOFile.docra(file);
                if(l.size()>0){
                    PhongHoc.setSma(l.get(l.size()-1).getMa()+1);
                }
            }
            ok=true;
        }
        jTextField1.setText(PhongHoc.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(them){
            PhongHoc p=new PhongHoc();
            p.setTen(jTextField2.getText());
            p.setSg(Integer.parseInt(jTextField3.getText()));
            p.setKieu(jComboBox1.getSelectedItem().toString());
            tm.addRow(p.toObject());
            them=false;
            buttonTrue();
        }
        if(sua){
            int r=jTable1.getSelectedRow();
            tm.setValueAt(jTextField2.getText(), r, 1);
            tm.setValueAt(jTextField3.getText(), r, 2);
            tm.setValueAt(jComboBox1.getSelectedItem(), r, 3);
            sua=false;
            buttonTrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

